import java.util.Scanner;
public class VariableExamples 
 {

    public static void main(String[] args) 
     {
        // Integer Type
        int Age = 18;

        // Float type for height in meters
        float Height = 1.75f;

        // Double Type
        double Income = 123456789.00;

        // Character Type
        char Grade = 'O'; // A Single Variable

        // String Type
        String Name = "BHAVIIK Parmar";

        // Boolean Type 
        boolean Darshan_Student = true; // For true/false value

        // Long Type Large Number 
        long Account_Number = 7800000000L; // Last Ma L Lakhavooo........

        // Byte Type 
        byte SmallNumber = 100; //  Small Number

        // Short Type
        short MediumNumber = 32000; // Short Number 
        
     }

 }
